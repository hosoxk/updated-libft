#include "libft.h"

size_t	ft_strlen_map(const char *s)
{
	int	i;

	if (s == NULL)
		return (0);
	i = 0;
	while (s[i] != '\0' && s[i] != '\r' && s[i] != '\n' && s[i] != '\t')
		i++;
	return (i);
}
